#!/bin/bash
# 持續監控爬取進度

while true; do
    clear
    echo "======================================================================"
    echo "NYCU 課程爬蟲 - 即時進度監控"
    echo "時間: $(date '+%Y-%m-%d %H:%M:%S')"
    echo "======================================================================"

    # 顯示最新進度
    tail -30 recrawl_output.log | grep "進度:" | tail -5

    echo ""
    echo "----------------------------------------------------------------------"
    echo "按 Ctrl+C 停止監控"
    echo "======================================================================"

    sleep 30
done
